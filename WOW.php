          <h2 id="exp">Expení</h2>
          <div class="pozadi"><section class="expeni">
              <p>Při prvním zpuštění hry se dostanete na výběr serveru což teď jsou už jenom dva druhy: Normal a Role Playing. Po vybrání serveru si budete vytvářet postavu. Na výběr nejprve máte rasu a potom classu, poté si budete Vaší postavu vzhledově upravovat a pojmenujete si ji.</p>
              <p>Pokud máte nový účet tak budete začánat v lokaci <strong>Exile's Reach</strong>. Toto je velice pěkně uděláná zóna pro začátečníky kde jsou vám vysvětlovány základy vaší classy a WoW obecně. Jste aktivně odměňováni za exploraci zóny a dělání side questů.</p>
              <p>Jakmile dokončíte <strong>Exile's Reach</strong> tak přiletíte před Stormwind/Ogrimar a dostanete prohlídku vašeho hlavního města. Poté zajdete za vůdcem vaší frakce a budete expit přes datadisk BfA.</p>
              <p>Jiný příběh je když už máte jednu postavu na lvl 50 tak si můžete vybrat startovací zónu na původní rassovou zónu. Expení probíhá přes <strong>Chromie Time</strong> vyberete si jakoukoliv expanzi a tam můžete vyexpit do lvl 50.</p>
              <p>Malý tip pokud expíte přes legion. Vaše artefakty mají sloty na upgrade, které vylepší jejich ilvl. Získávají se na konci důležitých questovích linek. Nezapoměňte si je nasadit ;) !</p>
              <p><strong>Řidičáky</strong> prošli pár změnami od BfA. Základní 60% řidičák si odemknete vrámci vaší prohlídky hlavním městem na lvl <strong>10</strong>. Na lvl <strong>20</strong> si budete moct odemknout 100% rychlost. Na lvl <strong>30</strong> si budete moct odemknout prvního létajícího mounta, ale žádná sláva to není je to jenom 150% rychlost. Pravá sranda začíná na lvl <strong>40</strong> kdy si budete moct koupit 310% rychlost.</p>
              <p>Pro ty pozornější co nemají alzheimera tak si možná řeknou: Co se stalo s 280% rychlostí? Odpověď: Stále je ve hře na postavách kteří tuto úroveň měli tak jím tato rychlost zůstává a musíte si stále koupit 310% rychlost.</p>
